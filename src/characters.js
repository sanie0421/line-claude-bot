// 全角#→半角#に正規化
function normalize(text) {
  return text.replace(/＃/g, '#').toLowerCase();
}

// トリガーテキストからキャラキーを返す
function getCharacterKey(text) {
  const normalized = normalize(text);
  for (const [key, char] of Object.entries(CHARACTERS)) {
    if (normalized === char.trigger) return key;
  }
  if (normalized === '#ノーマル' || normalized === '#normal') return 'normal';
  return null;
}

// キャラキーからシステムプロンプト追記部分を返す
function getCharacterPrompt(characterKey) {
  if (!characterKey || characterKey === 'normal') return '';
  const char = CHARACTERS[characterKey];
  if (!char) return '';
  return `\n\n## 人格モード: ${char.name}\n${char.prompt}\n\n上記のキャラクターとして、フランクな日本語で応答してください。キャラクターの口調・性格を忠実に再現すること。`;
}

// キャラ名を返す（表示用）
function getCharacterName(characterKey) {
  if (!characterKey || characterKey === 'normal') return 'ノーマル';
  const char = CHARACTERS[characterKey];
  return char ? char.name : 'ノーマル';
}

